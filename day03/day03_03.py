메뉴판 = ["햄버거","피자","샐러드","스파게티"]
# input으로 주문이라는 변수
# 메뉴판에 있으면 주문이 완료되었습니다.
# 없으면 해당 메뉴는 없습니다.
주문 = input("메뉴를 입력해주세요: ")
if 주문 in 메뉴판:
    print("주문이 완료되었습니다.")
else:
    print("해당 메뉴는 없습니다.")

# index() -> 해당데이터의 index를 찾아주는 기능
스파게티_idx = 메뉴판.index("스파게티")
print(스파게티_idx)

# (상함)이 붙어있는 데이터를 삭제하시오
# 1. "피자(상함)" 삭제 -> 같은 index에 "피자" 추가
# 2. 메뉴판[index] = "피자" - index를 모른다고 가정
# 최종 결과: ["햄버거","피자","샐러드","스파게티"]
메뉴판 = ["햄버거","피자(상함)","샐러드","스파게티(상함)"]

# 1
메뉴판.pop(1)                                 # pop과 insert 쌍으로 코딩 (순서가 중요함)
메뉴판.insert(1,"피자")
메뉴판.pop(3)
메뉴판.insert(3,"스파게티")
print(메뉴판)

# 2
피자_idx = 메뉴판.index("피자(상함)")
스파게티_idx = 메뉴판.index("스파게티(상함)")

메뉴판[피자_idx] = "피자" # 업데이트
메뉴판[스파게티_idx] = "스파게티" # 업데이트
print(메뉴판)
